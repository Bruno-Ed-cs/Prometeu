         </p>
         <p>
            Além disso, se a qualia epifenomenal não tem qualquer efeito no mundo físico, ele seria exatamente o mesmo quer ela exista ou não. A existência ou não existência da qualia se tornaria então tão relevante quanto a existência ou não existência de sacis-pererês invisíveis e sem massa pulando ao seu redor agora!  
         </p>
         <p>
            Apesar de com frequência parecer assim, talvez o que faz com que todas essas sensações arrebatadoras que você experiencia existam não precise ser alguma propriedade intrínseca e inacessível como a qualia. Para o Dennett elas são puramente a soma total de todas as associações e disposições inatas e desenvolvidas que são engatilhadas no seu cérebro quando você é informado pelos seus sentidos. 
         </p>
         <p>
            Logo, a sua cor favorita é bonita não apenas pelo que você é informado, mas pela forma que você é informado. Quando a contempla os circuitos neurais de experiências marcantes e disposições biológicas associadas a ela, que foram armazenadas na sua memória ou DNA, são ativadas no seu cérebro, intensificando e moldando a experiência atual. As cores são importantes para nós não por serem “de outro mundo” mas simplesmente porque foi vantajoso para os nossos ancestrais dar atenção a elas.  
         </p>
